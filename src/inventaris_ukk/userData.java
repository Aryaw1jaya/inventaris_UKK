    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        String klik = jTable1.getModel().getValueAt(row, 0).toString();
        String select = "SELECT * FROM petugas WHERE id_petugas = '"+klik+"'";

        try {
            pst = conn.prepareStatement(select);
            rs = pst.executeQuery();

            if (rs.next()){
                String tulisan1 = rs.getString("id_petugas");
                jTextField1.setText(tulisan1);

                String tulisan2 = rs.getString("nama_pet");
                jTextField2.setText(tulisan2);

                String tulisan5 = rs.getString("alamat");
                jTextField5.setText(tulisan5);

                String tulisan3 = rs.getString("no_telp");
                jTextField3.setText(tulisan3);

                String tulisan4 = rs.getString("password");
                jPasswordField1.setText(tulisan4);

            }
        } catch (SQLException ex) {
            System.out.println("Gagal Memilih Data");
        }
    }//GEN-LAST:event_jTable1MouseClicked

