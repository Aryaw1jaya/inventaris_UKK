private void Tampilkan_Data(){
        String perintah = "SELECT * FROM petugas";
        
        try {
            pst = conn.prepareStatement(perintah);
            rs =pst.executeQuery();
            DefaultTableModel tpelang = new DefaultTableModel();
            tpelang.addColumn("id_petugas");
            tpelang.addColumn("nama_pet");
            tpelang.addColumn("alamat");
            tpelang.addColumn("no_telp");
            tpelang.addColumn("password");
	    tpelang.addColumn("level");
	 while (rs.next()) {                
                tpelang.addRow(
                        new Object[]{
                            rs.getString(1),
                            rs.getString(2),
                            rs.getString(3),
                            rs.getString(4),
			    rs.getString(5),
			    rs.getString(6)
                        });
            }jTable1.setModel(tpelang);
            
        } catch (SQLException ex) {
            Logger.getLogger(userData.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
