    private void brg_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_brg_tableMouseClicked
        //Perintah menampilkan nama barang di field
        int row = brg_table.rowAtPoint(evt.getPoint());
        
        String kd = brg_table.getValueAt(row, 0).toString();
        String nama = brg_table.getValueAt(row, 1).toString();
        
        brgField.setText(nama);
        
        //Perintah menampilkan data di tabel bawah
        String statement = "SELECT * FROM detail_inv WHERE id_barang = '"+kd+"'";
        
        try {
            pst = conn.prepareStatement(statement);
            rs = pst.executeQuery();
            
            DefaultTableModel list2 = new DefaultTableModel();
            list2.addColumn("ID Detail");
            list2.addColumn("Merk Barang");
            list2.addColumn("Ketersediaan");
            list2.addColumn("Keterangan");
            list2.addColumn("Tanggal Masuk");
            
            while (rs.next()) {
                list2.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6)
                });
            }
            det_table.setModel(list2);
        } catch (SQLException ex) {
            Logger.getLogger(inventaris.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_brg_tableMouseClicked

    private void det_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_det_tableMouseClicked
        //Perintah menampilkan data detail di field
        int row = det_table.rowAtPoint(evt.getPoint());
        
        String merk = det_table.getValueAt(row, 1).toString();
        String ketersediaan = det_table.getValueAt(row, 2).toString();
        String keterangan = det_table.getValueAt(row, 3).toString();
        String tanggal = det_table.getValueAt(row, 4).toString();
        merkField.setText(merk);
        switch (ketersediaan) {
            case "1":
                avaField.setText("Ada");
                break;
            case "2":
                avaField.setText("Hilang");
                break;
            case "0":
                avaField.setText("Dipinjam");
                break;
            default:
                break;
        }
        //ketArea.setText(keterangan);
        tglField.setText(tanggal);
    }//GEN-LAST:event_det_tableMouseClicked

